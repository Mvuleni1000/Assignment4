public class ClassRoom {

    String table;
    String chairs;
    String students;
    String lecutures;

    public String getTable() {
        return table;
    }

    public String getChairs() {
        return chairs;
    }

    public String getStudents() {
        return students;
    }

    public String getLecutures() {
        return lecutures;
    }

    public void setTable(String table) {
        this.table = table;
    }

    public void setChairs(String chairs) {
        this.chairs = chairs;
    }

    public void setStudents(String students) {
        this.students = students;
    }

    public void setLecutures(String lecutures) {
        this.lecutures = lecutures;
    }
}
